// Helper function to generate all phone models from a models object
export function getAllPhoneModels(modelsObj: Record<string, string[]> | null | undefined): string[] {
  if (!modelsObj) return [];
  return Object.entries(modelsObj).flatMap(([brand, models]) =>
    models.map((model) => `${brand} ${model}`)
  );
}